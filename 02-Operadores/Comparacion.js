let b = 10;

console.log(b > 5); //! Mayor que
console.log(b >= 5); //! Mayor o igual que
console.log(b < 5); //! Menor que
console.log(b <= 5); //! Menor o igual que
console.log(b == 5); //! Igual que
console.log(b != 5); //! Diferente que
console.log(b === 5); //! Estrictamente igual que
console.log(b !== 5); //! Estrictamente diferente que
